#[derive(Copy, Clone, Eq, PartialEq, Ord, PartialOrd, Hash)]
pub enum Font {
    X9500,
    X9508,
}

pub fn font_char(byte: u8, font: Option<Font>) -> char {
    if byte < 128 {
        char::from(byte)
    } else {
        let table = match font {
            None => &SI,
            Some(Font::X9500) => &SO_9500X,
            Some(Font::X9508) => &SO_9508X,
        };
        table[usize::from(byte - 128)]
    }
}
